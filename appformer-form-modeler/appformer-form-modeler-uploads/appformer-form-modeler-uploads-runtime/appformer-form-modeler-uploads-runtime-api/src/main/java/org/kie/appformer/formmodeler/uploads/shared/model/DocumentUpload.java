package org.kie.appformer.formmodeler.uploads.shared.model;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.Id;

import org.jboss.errai.common.client.api.annotations.MapsTo;
import org.jboss.errai.common.client.api.annotations.Portable;

@Portable
@Entity
public class DocumentUpload implements Serializable {

    @Id
    private String documentId;

    private String name;

    private Long size;

    public DocumentUpload() {
    }

    public DocumentUpload(@MapsTo("documentId") String documentId,
                          @MapsTo("name") String name,
                          @MapsTo("size") Long size) {
        this.documentId = documentId;
        this.name = name;
        this.size = size;
    }

    public String getDocumentId() {
        return documentId;
    }

    public void setDocumentId(String documentId) {
        this.documentId = documentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getSize() {
        return size;
    }

    public void setSize(Long size) {
        this.size = size;
    }
}
